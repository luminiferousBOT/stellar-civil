function getRowHTML(row) {
  const isStraight = row.shape === 'straight';
  const isL = row.shape === 'l_shape';
  
  return `
    <tr data-id="${row.id}" style="border-bottom: 1px solid var(--border-color);">
      <td style="padding: 0.5rem;">
        <select class="calc-input row-input-shape" style="width: 100%; margin: 0; padding: 0.5rem;">
          <option value="straight" ${row.shape === 'straight' ? 'selected' : ''}>Straight (A)</option>
          <option value="l_shape" ${row.shape === 'l_shape' ? 'selected' : ''}>L-Shape (A,B)</option>
          <option value="u_shape" ${row.shape === 'u_shape' ? 'selected' : ''}>U-Shape (A,B,C)</option>
        </select>
      </td>
      <td style="padding: 0.5rem;">
        <select class="calc-input row-input-dia" style="width: 100%; margin: 0; padding: 0.5rem;">
          ${REBAR_DIAMETERS.map(d => `<option value="${d}" ${d === row.dia ? 'selected' : ''}>${d}</option>`).join('')}
        </select>
      </td>
      <td style="padding: 0.5rem;">
        <input type="number" class="calc-input row-input-mem" value="${row.mem}" min="1" step="1" style="width: 100%; margin: 0; padding: 0.5rem;">
      </td>
      <td style="padding: 0.5rem;">
        <input type="number" class="calc-input row-input-bars" value="${row.bars}" min="1" step="1" style="width: 100%; margin: 0; padding: 0.5rem;">
      </td>
      <td style="padding: 0.5rem;">
        <input type="number" class="calc-input row-input-a" value="${row.a}" min="0" step="10" placeholder="A" style="width: 100%; margin: 0; padding: 0.5rem;">
      </td>
      <td style="padding: 0.5rem;">
        <input type="number" class="calc-input row-input-b" value="${row.b}" min="0" step="10" placeholder="B" style="width: 100%; margin: 0; padding: 0.5rem;" ${isStraight ? 'disabled' : ''}>
      </td>
      <td style="padding: 0.5rem;">
        <input type="number" class="calc-input row-input-c" value="${row.c}" min="0" step="10" placeholder="C" style="width: 100%; margin: 0; padding: 0.5rem;" ${isStraight || isL ? 'disabled' : ''}>
      </td>
      <td style="padding: 0.5rem; text-align: center;">
        <button class="btn btn-secondary btn-del-row" style="padding: 0.5rem; color: #ef4444;" title="Delete Row">
          <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><polyline points="3 6 5 6 21 6"></polyline><path d="M19 6v14a2 2 0 0 1-2 2H7a2 2 0 0 1-2-2V6m3 0V4a2 2 0 0 1 2-2h4a2 2 0 0 1 2 2v2"></path></svg>
        </button>
      </td>
    </tr>
  `;
}

function renderRows(container) {
  const tbody = container.querySelector('#bbs-tbody');
  tbody.innerHTML = bbsRows.map(getRowHTML).join('');
  
  // Attach shape change listeners to disable inactive fields
  tbody.querySelectorAll('.row-input-shape').forEach(sel => {
    sel.addEventListener('change', (e) => {
      const tr = e.target.closest('tr');
      const shape = e.target.value;
      const bInput = tr.querySelector('.row-input-b');
      const cInput = tr.querySelector('.row-input-c');
      
      if (shape === 'straight') {
        bInput.disabled = true;
        cInput.disabled = true;
      } else if (shape === 'l_shape') {
        bInput.disabled = false;
        cInput.disabled = true;
      } else {
        bInput.disabled = false;
        cInput.disabled = false;
      }
    });
  });
  
  // Delete handlers
  tbody.querySelectorAll('.btn-del-row').forEach(btn => {
    btn.addEventListener('click', (e) => {
      const tr = e.target.closest('tr');
      const id = parseInt(tr.getAttribute('data-id'));
      bbsRows = bbsRows.filter(r => r.id !== id);
      renderRows(container);
    });
  });
}

function syncStateFromDOM(container) {
  const trs = container.querySelectorAll('#bbs-tbody tr');
  const newRows = [];
  
  trs.forEach(tr => {
    newRows.push({
      id: parseInt(tr.getAttribute('data-id')),
      shape: tr.querySelector('.row-input-shape').value,
      dia: parseInt(tr.querySelector('.row-input-dia').value) || 12,
      mem: parseInt(tr.querySelector('.row-input-mem').value) || 1,
      bars: parseInt(tr.querySelector('.row-input-bars').value) || 1,
      a: parseFloat(tr.querySelector('.row-input-a').value) || 0,
      b: parseFloat(tr.querySelector('.row-input-b').value) || 0,
      c: parseFloat(tr.querySelector('.row-input-c').value) || 0
    });
  });
  
  bbsRows = newRows;
}

function calculateBBS(container) {
  syncStateFromDOM(container);
  saveInputs(MODULE_ID, { rows: bbsRows });
  
  if (bbsRows.length === 0) {
    showToast('Add at least one row to calculate.', 'error');
    return;
  }
  
  let totalBBSWeight = 0;
  const weightByDia = {}; // { 12: 54.3, 16: 102.5 }
  
  let detailsHtml = `
    <div style="overflow-x: auto; margin-top: 1.5rem;">
      <table style="width: 100%; border-collapse: collapse; text-align: left;">
        <thead>
          <tr style="background: var(--bg-hover); border-bottom: 2px solid var(--border-color);">
            <th style="padding: 0.75rem;">Item</th>
            <th style="padding: 0.75rem;">Shape</th>
            <th style="padding: 0.75rem;">Cut Length (mm)</th>
            <th style="padding: 0.75rem;">Total Length (m)</th>
            <th style="padding: 0.75rem;">Weight (kg)</th>
          </tr>
        </thead>
        <tbody>
  `;
  
  bbsRows.forEach((row, idx) => {
    let cutLen = 0;
    
    // Cutting length calculation with bend deductions (2d per 90deg bend)
    if (row.shape === 'straight') {
      cutLen = row.a;
    } else if (row.shape === 'l_shape') {
      cutLen = row.a + row.b - (2 * row.dia); // 1 bend of 90 deg
    } else if (row.shape === 'u_shape') {
      cutLen = row.a + row.b + row.c - 2 * (2 * row.dia); // 2 bends of 90 deg
    }
    
    if (cutLen < 0) cutLen = 0;
    
    const totalLenMeters = (cutLen * row.mem * row.bars) / 1000;
    const unitWt = getRebarWeight(row.dia);
    const weight = totalLenMeters * unitWt;
    
    totalBBSWeight += weight;
    
    if (!weightByDia[row.dia]) weightByDia[row.dia] = 0;
    weightByDia[row.dia] += weight;
    
    detailsHtml += `
      <tr style="border-bottom: 1px solid var(--border-color);">
        <td style="padding: 0.75rem;">Row ${idx + 1}</td>
        <td style="padding: 0.75rem; text-transform: capitalize;">${row.shape.replace('_', ' ')} (Φ${row.dia})</td>
        <td style="padding: 0.75rem;">${cutLen.toFixed(0)}</td>
        <td style="padding: 0.75rem;">${totalLenMeters.toFixed(2)}</td>
        <td style="padding: 0.75rem; font-weight: bold;">${weight.toFixed(2)}</td>
      </tr>
    `;
  });
  
  detailsHtml += `</tbody></table></div>`;
  
  // Summary by Dia
  let summaryHtml = `
    <h4 style="margin: 1.5rem 0 1rem;">Summary by Diameter</h4>
    <div style="display: flex; gap: 1rem; flex-wrap: wrap;">
  `;
  
  Object.keys(weightByDia).sort((a,b) => a-b).forEach(dia => {
    summaryHtml += `
      <div style="padding: 1rem; background: var(--bg-hover); border: 1px solid var(--border-color); border-radius: var(--radius-sm); min-width: 150px;">
        <div style="font-size: 0.9rem; color: var(--text-muted);">Φ${dia} mm</div>
        <div style="font-size: 1.25rem; font-weight: bold; color: var(--color-primary);">${weightByDia[dia].toFixed(2)} kg</div>
      </div>
    `;
  });
  summaryHtml += `</div>`;
  
  const resultsDiv = container.querySelector('#results-container');
  resultsDiv.innerHTML = `
    <div class="results-panel">
      <div class="results-panel__header">
        <h3>Schedule Output</h3>
      </div>
      
      <div style="padding: 1.5rem; background: var(--bg-hover); border: 2px solid var(--primary-color); border-radius: var(--radius-md); text-align: center; margin-bottom: 1.5rem;">
        <h4 style="margin-bottom: 0.5rem; font-weight: normal; color: var(--text-muted);">Total Reinforcement Weight</h4>
        <div style="font-size: 2.5rem; font-weight: bold; color: var(--primary-color);">
          ${totalBBSWeight.toFixed(2)} kg
        </div>
      </div>
      
      ${summaryHtml}
      ${detailsHtml}
    </div>
  `;
  
  updateStickyBar([
    { label: 'Total Weight', value: totalBBSWeight.toFixed(2) + ' kg' },
    { label: 'Items', value: bbsRows.length.toString() }
  ]);
}

export function initBbsGenerator(container) {
  container.innerHTML = baseContent;
  
  const customActions = `
    <div style="display: flex; gap: 1rem; width: 100%;">
      <button id="add-btn" class="btn btn-secondary">
        + Add Row
      </button>
      ${renderActionBar()}
    </div>
  `;
  container.querySelector('#action-bar-container').innerHTML = customActions;
  
  const saved = loadInputs(MODULE_ID);
  if (saved && saved.rows && Array.isArray(saved.rows) && saved.rows.length > 0) {
    bbsRows = saved.rows;
    // ensure IDs don't overlap on new Add
    rowIdCounter = Math.max(...bbsRows.map(r => r.id)) + 1;
  } else {
    // defaults
    bbsRows = [
      { id: 1, shape: 'straight', dia: 12, mem: 1, bars: 4, a: 3000, b: 0, c: 0 },
      { id: 2, shape: 'l_shape', dia: 12, mem: 1, bars: 4, a: 3000, b: 200, c: 0 }
    ];
    rowIdCounter = 3;
  }
  
  renderRows(container);
  
  container.querySelector('#add-btn').addEventListener('click', () => {
    syncStateFromDOM(container);
    bbsRows.push({
      id: rowIdCounter++,
      shape: 'straight',
      dia: 12,
      mem: 1,
      bars: 1,
      a: 1000,
      b: 0,
      c: 0
    });
    renderRows(container);
  });
  
  container.querySelector('#btn-calculate').addEventListener('click', () => {
    calculateBBS(container);
  });
  
  container.querySelector('#btn-clear').addEventListener('click', () => {
    bbsRows = [];
    renderRows(container);
    container.querySelector('#results-container').innerHTML = '';
    updateStickyBar([]);
    localStorage.removeItem("stellar_civil_inputs_" + MODULE_ID);
  });
}
